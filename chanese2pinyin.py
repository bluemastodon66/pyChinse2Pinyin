    def trand2simple(self,s,encoding="UTF-8"):
        str = s.decode(encoding)
        l = len(str)
        buf = ""
        arr = []
        for i in range(l):
            c = str[i].encode(encoding)
            if not len(c) > 1:
                buf += c
            else:
                if c in self._trad_tab:
                    c = self._trad_tab[ c ]
                if buf != "":
                    arr.append(buf)
                    buf = ""
                arr.append(c)
        if buf !="":
            arr.append(buf)
        ret = "-".join(arr)
        return ret.decode("utf-8").encode("gb2312")
    def _getParts(self, num):
        if num>0 and num<160:
            return chr(num)
        else:
            if num<-20319 or num>-10247:
                return ""
            else:
                ll = len(self._ascii) -1
                for i in reversed(range(ll)):
                    if self._ascii[i][1]<=num:
                        break
                return self._ascii[i][0]
    def trand2pinyin(self, chs):
        strings = self.trand2simple(chs)
        l = len(strings)
        ret = ""
        counter = 0
        for i in range(l):
            c =  strings[counter]
            p = ord(c)
            if p > 160:
                counter+=1
                extc = strings[counter]
                q = ord(extc)
                p = p*256+q-65536
            ret +=  self._getParts(p)

            counter+=1
            if counter >= l:
                break
        return ret


